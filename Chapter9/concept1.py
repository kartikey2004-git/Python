import matplotlib.pyplot as py

# Data
x = [1, 2, 3, 4, 5]
y = [1, 4, 9, 16, 25]

# Plot
py.plot(x, y)

# Adding title and labels
py.title("Simple Line Plot")
py.xlabel("x values")
py.ylabel("y values")

# Show plot
py.show()

